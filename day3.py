

def day3_part1(input_list, right_amount, down_amount):
    x, y, tree_hit_count = 0, 0, 0
    x_length = len(input_list[0])
    y_length = len(input_list)
    while y + down_amount < y_length:
        x = (x + right_amount) % x_length
        y = (y + down_amount)
        if input_list[y][x] == '#':
            tree_hit_count = tree_hit_count + 1

    return tree_hit_count


def day3_part2(input_list, amounts):
    result = 1
    for amount in amounts:
        right_amount, down_amount = amount
        result = result * day3_part1(input_list, right_amount, down_amount)
    return result


